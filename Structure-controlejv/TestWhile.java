public class TestWhile{
    public static void main(String[] args){
        int n=2;
        int i=1;
        int result;
        while(i>0 && i<11){
            result=n*i;
            System.out.println(n+"x"+i+"="+result);
            i++;
        }
        System.out.println("Merci d'avoir utilise notre programme");

    }
}